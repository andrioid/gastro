// rewriteFrontmatter transforms code-gen markers in the frontmatter:
// - gastro.Context() lines are stripped (the handler template generates ctx)
// - gastro.Props[T]() lines are rewritten to alias the generated __props variable
// - Type declarations are stripped for components (they're hoisted to package level)
func rewriteFrontmatter(frontmatter string, info *FrontmatterInfo) string {
	lines := strings.Split(frontmatter, "\n")
	var kept []string
	inType := false
	braceDepth := 0

	for _, line := range lines {
		trimmed := strings.TrimSpace(line)

		// Skip gastro.Context() — it's generated in the handler template
		if strings.Contains(trimmed, "gastro.Context()") {
			continue
		}

		// For components, skip type declarations (hoisted to package level)
		if info.IsComponent {
			if !inType && strings.HasPrefix(trimmed, "type ") {
				inType = true
				braceDepth = 0
			}
			if inType {
				braceDepth += strings.Count(line, "{") - strings.Count(line, "}")
				if braceDepth <= 0 {
					inType = false
				}
				continue
			}
		}

		// Rewrite gastro.Props[T]() — replace with alias to generated __props
		if strings.Contains(trimmed, "gastro.Props[") {
			// Extract the variable name from "varname := gastro.Props[T]()"
			if idx := strings.Index(trimmed, ":="); idx > 0 {
				varName := strings.TrimSpace(trimmed[:idx])
				kept = append(kept, varName+" := __props")
			}
			continue
		}

		kept = append(kept, line)
	}

	return strings.TrimSpace(strings.Join(kept, "\n"))
}

// ExportedComponentName derives an exported name from a component function name.
// For example: "componentPostCard" -> "PostCard"
func ExportedComponentName(funcName string) string {
	return strings.TrimPrefix(funcName, "component")
}

// StructField represents a field in a parsed Props struct.
type StructField struct {
	Name string
	Type string
}

// ParseStructFields extracts field names and types from a hoisted type
// declaration string like "type FooProps struct {\n    Title string\n    Count int\n}".
func ParseStructFields(hoistedTypes string) []StructField {
	lines := strings.Split(hoistedTypes, "\n")
	var fields []StructField
	inStruct := false

	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		if strings.Contains(trimmed, "struct {") {
			inStruct = true
			continue
		}
		if inStruct && trimmed == "}" {
			break
		}
		if !inStruct {
			continue
		}

		// Parse "FieldName FieldType" lines
		parts := strings.Fields(trimmed)
		if len(parts) >= 2 {
			fields = append(fields, StructField{
				Name: parts[0],
				Type: parts[1],
			})
		}
	}

	return fields
}

// HandlerFuncName derives a Go function name from a .gastro file path.
// For pages: "pages/index.gastro" -> "pageIndex"
// For components: "components/card.gastro" -> "componentCard"
func HandlerFuncName(filename string) string {
	name := filename
	name = strings.TrimSuffix(name, ".gastro")
	name = strings.ReplaceAll(name, "[", "")
	name = strings.ReplaceAll(name, "]", "")

	// Determine prefix based on directory
	var prefix string
	if strings.HasPrefix(name, "pages/") {
		prefix = "page"
		name = strings.TrimPrefix(name, "pages/")
	} else if strings.HasPrefix(name, "components/") {
		prefix = "component"
		name = strings.TrimPrefix(name, "components/")
	} else {
		prefix = "gastro"
	}

	// Split on / and - to create camelCase segments
	parts := strings.FieldsFunc(name, func(r rune) bool {
		return r == '/' || r == '-'
	})

	var result strings.Builder
	result.WriteString(prefix)

	for _, part := range parts {
		if part == "" {
			continue
		}
		result.WriteString(strings.ToUpper(part[:1]) + part[1:])
	}

	return result.String()
}
